#include "binary_trees.h"

/**
 * avl_remove - builds an avl tree from an array
 * @root: pointer to first element of the array
 * @value: number of elements in the array
 * Return: pointer to root node of created
 */

avl_t *avl_remove(avl_t *root, int value)
{
	if (root == NULL || value == 0)
		return (NULL);
	return (root);
}
